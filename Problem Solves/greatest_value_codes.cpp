


//Take two int values from the user and print the greatest among them.

#include <iostream>
using namespace std;

int main()
{
    int value1,value2;
    cout<<"Give value 1: ";
    cin>>value1;
    cout<<"Give value 2: ";
    cin>>value2;

    if(value1>value2)
    {
        cout<<"Value 1 is greatest";
    }
    else
    {
        cout<<"Value 2 is the greatest";
    }

}